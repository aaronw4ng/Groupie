package csci310;

public class Venue {
    String name;
    String address;
    String city;
    String state;
    String country;

    public Venue(String n, String a, String ci, String s, String co) {
        this.name = n;
        this.address = a;
        this.city = ci;
        this.state = s;
        this.country = co;
    }
}
